package ch06_ex02.center.utils;

public class InitSampleData {
	
	private String[] mIds;
	private String[] mPws;
    private String[] mNames;
    private String[] mPhones;
    private String[] mMails;
    private String[] mSubjects;
    
	public String[] getmIds() {
		return mIds;
	}
	public void setmIds(String[] mIds) {
		this.mIds = mIds;
	}
	public String[] getmPws() {
		return mPws;
	}
	public void setmPws(String[] mPws) {
		this.mPws = mPws;
	}
	public String[] getmNames() {
		return mNames;
	}
	public void setmNames(String[] mNames) {
		this.mNames = mNames;
	}
	public String[] getmPhones() {
		return mPhones;
	}
	public void setmPhones(String[] mPhones) {
		this.mPhones = mPhones;
	}
	public String[] getmMails() {
		return mMails;
	}
	public void setmMails(String[] mMails) {
		this.mMails = mMails;
	}
	public String[] getmSubjects() {
		return mSubjects;
	}
	public void setmSubjects(String[] mSubject) {
		this.mSubjects = mSubject;
	} 
}
